#include<unistd.h>

int	main(int ac, char **av)
{
	int	y;
	int	x;

	x = 1;
	y = 0;
	if (ac > 1)
	{
		while (x < ac)
		{
			y = 0;
			while (av[x][y] != '\0')
			{
				write(1, &av[x][y], 1);
				y++;
			}
			write(1, "\n", 1);
			x++;
		}
	}
	return (0);
}
